/*
 * log.c
 *
 * Helpers to print message with various priorities.
 */

#include <kernel/log.h>

// ============================================================================
// ----------------------------------------------------------------------------
// ============================================================================

enum log_level g_log_level = LOG_INFO;

// ============================================================================
// ----------------------------------------------------------------------------
// ============================================================================

void log_set_level(enum log_level level)
{
  g_log_level = level;
}

// ----------------------------------------------------------------------------

enum log_level log_get_level(void)
{
  return g_log_level;
}

