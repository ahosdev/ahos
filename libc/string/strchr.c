/*
 * strchr.c
 */

#include <stddef.h>

// ============================================================================
// ----------------------------------------------------------------------------
// ============================================================================

char *strchr(const char *s, int c)
{
	char *p = (char*) s;

	while (*p) {
		if (*p == (char)c) {
			return p;
		}
		p++;
	}

	return NULL;
}

